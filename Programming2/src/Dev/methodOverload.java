package Dev;

public class methodOverload {

	
	public static void matt(int test) {
		System.out.println("1");
	}
	public static void matt(double beno) {
		System.out.println("2");
	}
	public static void matt(String test) {
		System.out.println("3");
	}
	public static void matt(double beno, int test2) {
		System.out.println("4");
	}
}
